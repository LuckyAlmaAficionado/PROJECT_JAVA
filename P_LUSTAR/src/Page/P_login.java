    private boolean checkRegister(){
        String first_name = txt_firstname.getText().toString();
        String last_name = txt_lastname.getText().toString();
        String User = sign_username.getText().toString();
        String Pass = sign_pass.getText().toString();
        String conPass = sign_conpass.getText().toString();
        String sql = "SELECT * FROM `tb_karyawan` WHERE `username` LIKE '"+ User +"'";
        try{
            Statement s = conn.createStatement();
            PreparedStatement ps = conn.prepareStatement(sql);
            ResultSet rs = ps.executeQuery(sql);
            if (first_name.equals("") || last_name.equals("") || User.equals("") || Pass.equals("")) {
                warning_sign.setText("*Tabel still empty");
            }else if(rs.next()){
                warning_sign.setText("*Username already exists");
            }else if(!Pass.equals(conPass)){
                warning_sign.setText("*Password not same");
            }else{
                return true;
            }
        }catch(Exception e){
            System.out.println("Error: " + e.getMessage());
        }
        return false;
    }
    
