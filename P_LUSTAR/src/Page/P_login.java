    private String checklogin(){
        String user = txt_username.getText().toString();
        String pass = txt_password.getText().toString();
        try{
            Statement s = conn.createStatement();
            String sql = "SELECT * FROM `tb_karyawan` WHERE `username` LIKE '"+ user +"' AND `password` LIKE '"+ pass +"'";
            PreparedStatement pst = conn.prepareStatement(sql);
            ResultSet rs = pst.executeQuery(sql);
            if (rs.next()) {
                id = rs.getString("id_karyawan");
                return id;
            }
        }catch(Exception e){
            System.out.println("Error Message: " + e.getMessage());
        }
        return "";
    }
    
    public String getIdNumber(){
        String query = "SELECT id_karyawan FROM tb_karyawan";
        String temp = null;
        try {
            Statement st = conn.createStatement();
            ResultSet rs = st.executeQuery(query);
            while (rs.next()) {                
                temp = rs.getString("id_karyawan");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error: " + e.getMessage());
        }
        return temp;
    }
    
